package com.project.housekeeping.pojo;


public class Professional {

  private long id;
  private String healthFront;
  private String healthVerso;
  private String professional;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getHealthFront() {
    return healthFront;
  }

  public void setHealthFront(String healthFront) {
    this.healthFront = healthFront;
  }


  public String getHealthVerso() {
    return healthVerso;
  }

  public void setHealthVerso(String healthVerso) {
    this.healthVerso = healthVerso;
  }


  public String getProfessional() {
    return professional;
  }

  public void setProfessional(String professional) {
    this.professional = professional;
  }

}
